<template>
    <div class="container">
        <app-header :quoteCount="quotes.length" :maxQuotes="maxQuotes"></app-header>
        <app-new-quote @quoteWasCreated="addQuote"></app-new-quote>
        <app-quotes-grid :quotes="quotes" @quoteWasDeleted="deleteQuote"></app-quotes-grid>
        <div class="row">
            <div class="col-sm-12 text-center">
                <div class="alert alert-info">
                    Info: Click on a Quote to delete it!
                </div>
            </div>
        </div>
    </div>
</template>

<script>
    import QuotesGrid from './components/QuotesGrid.vue';
    import NewQuote from './components/NewQuote.vue';
    import Header from './components/Header.vue';
    
    export default {
        data() {
            return {
                quotes: ['Life is wonderful'],
                maxQuotes: 10
            }
        },
        components: {
            AppQuotesGrid: QuotesGrid,
            AppNewQuote: NewQuote,
            AppHeader: Header
        },
        methods: {
            addQuote(quote) {
                if (this.quotes.length >= this.maxQuotes) {
                    return alert('Please delete a quote first!');
                }
                this.quotes.push(quote);
            },
            deleteQuote(index) {
                this.quotes.splice(index,1);
            }
        }
        
    }
</script>

